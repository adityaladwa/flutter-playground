import 'package:flutter/material.dart';

class HomePage extends StatelessWidget {
  final items = ["Layout"];

  @override
  Widget build(BuildContext context) {
    return ListView.builder(
      itemCount: items.length,
      itemBuilder: (context, index) {
        return ListTile(
          title: Text(items[index]),
        );
      },
    );
  }
}
